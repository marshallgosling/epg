<h1 class="page-header">素材导入帮助手册</h1>
<p>本文主要介绍两种情况: </p>
<ul> <li>同步和扫描素材入库（已有素材信息，无素材文件路径）</li> <li>添加全新素材入库（无素材信息）</li> </ul>
<h2>1. 同步和扫描素材入库</h2>
<h3>检查素材记录信息</h3>
<p>找到素材状态为不可用的记录</p>
<p><img src="/images/help/material/编单系统-物料管理-1.png" /></p>
<p>点击“编辑”按钮，可以查看素材是否有文件路径信息</p>
<p><img src="/images/help/material/编单系统-物料管理-2.png" /></p>

<h3>准备及检查素材文件（mxf格式文件）</h3>
<p>准备好相应的素材文件，mxf格式的媒体文件，复制到相应的目录<br/>可用的目录有:</p>
<figure class="highlight"><pre><code class="language-html" data-lang="html">
Y:\MV\
Y:\宣传片垫片\
Y:\卡通\
Y:\电视剧\
Y:\电影\
Y:\音综\
Y:\其他\
</code></pre></figure>

<p>需要注意的是：</p>
<ul>
<li>文件夹必须是上述列出的位置</li> <li>文件名必须是以 <code>素材名称</code>.<code>播出编号</code>.<code>mxf</code> 统一命名规则 </li>
</ul>
<p><img src="/images/help/material/1708692865086.jpg" /></p>

<h3>批量选择</h3>
<p>确认好素材文件已经复制到指定的目录以后，就可以进行批量同步操作了</p>
<p><img src="/images/help/material/编单系统-物料管理-3.png" /></p>
<p>点击完以后，就可以等待通知结果，查看同步信息。<br/>如果同步成功，素材和对应节目库里的记录状态都会变为“可用”状态</p>
<p><img src="/images/help/material/编单系统-物料管理-4.png" /></p>

